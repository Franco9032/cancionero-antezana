ESTO SE REPITE UNA Y OTRA VEZ PARA LA PARTE RECITADA

A usted señor ciudadano, habitante de mi patria,
sembrador o militar, estudiante o industrial,
campesino, provinciano y al inquieto capitalino
a todos les digo amigos que esta tierra que habitamos
tenemos que respetarla
porque es ella quien nos da todo y solo nos pide amarla
y si queremos darle a nuestros hijos un futuro, un mañana
todos debemos poner el hombro para cuidarla;
yo sé que desde afuera viene el trigo,
también los desechos químicos
y que con 2 o 3 regalos siempre quieren manejarnos
imponiendo condiciones, con oscuras intenciones,
señoreando su grandeza, diciendo que aquí hay pobreza,
ya quisieran tener ellos nuestras selvas, nuestros ríos,
nuestras montañas y llanos, nuestro imponente altiplano y el aire que respiramos;
tierra fértil de hombres fuertes ya ven mis queridos hermanos,
es inmensa esta riqueza y es por eso que el de afuera en mi tierra se interesa;
que están estudiando al campesino con psicólogos entendidos
porque no van a estudiar a su abuela
que hasta ahora no han entendido
que esta tierra es de nosotros y no de los entremetidos..





